    public void gunnerEndingPlot() {

        promptSeparatorHandler.promptSeparatorResized();
        gunnerAfterArea3();

        System.out.println("┌────────────────────────────────────────────────┐");
        System.out.println("│   The obsidian tablet hums with unseen power   │");
        System.out.println("│  Will you confront its message, or turn away?  │");
        System.out.println("│                     (y/n)                      │");
        System.out.println("└────────────────────────────────────────────────┘");
        System.out.println("-->| ");
        String choice = scanner.nextLine();

        if(choice.equalsIgnoreCase("y")) {
            promptSeparatorHandler.promptSeparatorResized();
            gunnerSacrifice();
        } else {
            promptSeparatorHandler.promptSeparatorResized();
            gunnerLoop();
        }

    }

    public void gunnerPlotNarration(String[] array) {
        
        System.out.println("┌────────────────────────────────────┐");
        System.out.println("│   Narration Ahead! Not Skippable   │");
        System.out.println("└────────────────────────────────────┘");
        System.out.println("│  Press ENTER to continue  │");
        System.out.println("└───────────────────────────┘");
        scanner.nextLine();
        System.out.println();

        for (int i = 0; i < array.length; i++) {
            printDelayHandler.printWithDelay(array[i], 10); 
            System.out.println("\n");
        }

        System.out.println();
    }
