public class Mage {
    private int hp;
    private int attack;
    private int mana;
    private int defense;
    private int speed;
    private int level;
    private int experience;
    private String name;
    private String charClass;
    private String weapon;

    public Mage() {
        this.hp = 4000;
        this.attack = 550;
        this.mana = 1000;
        this.defense = 180;
        this.speed = 100;
        this.level = 1;
        this.experience = 0;
        this.name = "Aria Caelith";
        this.charClass = "Mage";
        this.weapon = "Wooden Staff";
    }

}